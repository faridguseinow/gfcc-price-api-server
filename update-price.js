const ftp = require('basic-ftp');
const iconv = require('iconv-lite');
const xml2js = require('xml2js');
const fs = require('fs');
const path = require('path');
const nodemailer = require('nodemailer');
const mongoose = require('mongoose');

// ===== MongoDB =====
const ProductSchema = new mongoose.Schema({
  id: { type: String, unique: true },
  category: String,
  name: String,
  wholesalePrice: Number,
  extraPrice: Number,
  retailPrice: Number,
  updatedAt: { type: Date, default: Date.now }
});
const Product = mongoose.model("Product", ProductSchema);

// ===== FTP / FILES =====
const LOCAL_XML_FILE = path.join(__dirname, 'farid_gold.xml');

// ===== SMTP =====
const transporter = nodemailer.createTransport({
  host: 'smtp.yandex.ru',
  port: 465,
  secure: true,
  auth: {
    user: process.env.EMAIL_USER,
    pass: process.env.EMAIL_PASS
  }
});

const notifyError = async (subject, message) => {
  try {
    await transporter.sendMail({
      from: `"Auto FTP Sync" <${process.env.EMAIL_USER}>`,
      to: process.env.EMAIL_TO,
      subject,
      text: message
    });
  } catch (err) {
    console.error("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ email:", err.message);
  }
};

// ===== MAIN LOGIC =====
async function fetchAndUpdateDB() {
  const client = new ftp.Client();
  client.ftp.verbose = false;

  try {
    console.log('üîÑ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ MongoDB...');
    await mongoose.connect(process.env.MONGO_URI);

    console.log('üîÑ –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ FTP...');
    await client.access({
      host: "192.168.7.108",
      port: 21,
      user: "farid_gold",
      password: "FaridGold2025#",
      secure: false
    });

    console.log('üì• –°–∫–∞—á–∏–≤–∞–Ω–∏–µ XML...');
    await client.downloadTo(LOCAL_XML_FILE, "farid_gold.xml");

    const buffer = fs.readFileSync(LOCAL_XML_FILE);
    const xml = iconv.decode(buffer, "win1251");

    const parsed = await xml2js.parseStringPromise(xml, { explicitArray: false });
    const items = parsed?.PriceList?.Goods?.Item;

    if (!items || !Array.isArray(items)) throw new Error("‚õî –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç XML");

    console.log(`üìä –ù–∞–π–¥–µ–Ω–æ —Ç–æ–≤–∞—Ä–æ–≤: ${items.length}`);

    // –ü—Ä–∏–≤–µ–¥–µ–Ω–∏–µ –∫ JSON
    const normalized = items.map(item => ({
      id: item.$.ID,
      category: item.$.ParentName,
      name: item.$.Name,
      wholesalePrice: parseFloat(item.$.Price.replace(",", ".")) || 0,
      extraPrice: parseFloat(item.$.PriceExt.replace(",", ".")) || 0,
      retailPrice: parseFloat(item.$.PriceRetail.replace(",", ".")) || 0,
      updatedAt: new Date()
    }));

    // –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –≤ MongoDB
    let updated = 0;
    for (const product of normalized) {
      await Product.updateOne(
        { id: product.id },
        { $set: product },
        { upsert: true }
      );
      updated++;
    }

    console.log(`‚úÖ –ü—Ä–∞–π—Å –æ–±–Ω–æ–≤–ª—ë–Ω. –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ/–æ–±–Ω–æ–≤–ª–µ–Ω–æ: ${updated} –ø–æ–∑–∏—Ü–∏–π`);

  } catch (err) {
    const errorMessage = `‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –ø—Ä–∞–π—Å–∞:\n${err.message}`;
    console.error(errorMessage);
    await notifyError('‚ùóÔ∏è–û—à–∏–±–∫–∞ –≤ –∞–≤—Ç–æ–æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –ø—Ä–∞–π—Å-–ª–∏—Å—Ç–∞', errorMessage);
  } finally {
    client.close();
    await mongoose.disconnect();
    process.exit(0);
  }
}

fetchAndUpdateDB();
